function suggestProfessions() {
  const answers = document.querySelectorAll("input[type=radio]:checked");
  const selectedProfessions = new Set();

  answers.forEach((answer) => {
    const selectedIds = professionMap[answer.value];
    if (selectedIds) {
      selectedIds.forEach((id) => selectedProfessions.add(id));
    }
  });

  const suggestedProfessions = professions.filter((profession) =>
    selectedProfessions.has(profession.id)
  );
  const suggestionsList = document.getElementById("suggestions");

  while (suggestionsList.firstChild) {
    suggestionsList.removeChild(suggestionsList.firstChild);
  }

  suggestedProfessions.forEach((profession) => {
    const listItem = document.createElement("li");
    listItem.innerHTML = `<h3>${profession.name}</h3><p>${profession.description}</p>`;
    suggestionsList.appendChild(listItem);
  });
}

